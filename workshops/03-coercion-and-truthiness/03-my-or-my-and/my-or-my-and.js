function myOr(dew, drop, sky) {
  if (dew) {
    return dew;
  }
  if (drop) {
    return drop;
  }
  if (sky) {
    return sky;
  }
  return sky;
}

function myAnd(we, us, our) {
  if (!we) {
    return we;
  }
  if (!us) {
    return us;
  }
  if (!our) {
    return our;
  }
  return our;
}
